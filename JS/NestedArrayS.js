//Nested arrays example
const nestedArrays = [
  [1, 2, 3],
  [4, 5, 6],
  [7, 8, 9]
];  
console.log('Nested Arrays:', nestedArrays);

//Accessing elements in nested arrays
console.log('Element at [1][2]:', nestedArrays[1][2]); // Outputs 6
console.log('Element at [0][0]:', nestedArrays[0][0]); // Outputs 1